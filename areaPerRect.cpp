// This program does area and perimeter of a rectangle
#include <cmath>
#include <iostream>

int main() {
    std::cout << "A rectangle has a dimensions:" << std::endl;
    std::cout << "length = 10cm and width = 6cm" << std::endl;
    std::cout << "Area is:" << (10 * 6) << "cm^2" << std::endl;
    std::cout << "Perimeter is:" << ((10+10) +(6+6)) << "cm" << std::endl;
}
